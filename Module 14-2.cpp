#include <iostream>

//функция рисования символа на игровом поле
void draw_symbol(char arr[][13], char arrX[][3], char arr0[][3], int x, int y, char player) {
	int xDisplay = (x - 1) * 4 + 1;
	int yDisplay = (y - 1) * 4 + 1;
		if (player == 'X') {
			for (int i = 0; i < 3; ++i) {
				for (int j = 0; j < 3; ++j) {
					arr[yDisplay + i][xDisplay + j] = arrX[i][j];
				}
			}
		}
		else {
			for (int i = 0; i < 3; ++i) {
				for (int j = 0; j < 3; ++j) {
					arr[yDisplay + i][xDisplay + j] = arr0[i][j];
				}
			}
		}
	return;
}

// функция вывода на экран игрового поля
void dispay_playing_field(char arr[][13])
{
	for (int i = 0; i < 13; ++i)
	{
		for (int j = 0; j < 13; ++j)
		{
			std::cout << arr[i][j];
		}
		std::cout << std::endl;
	}
	return;
}

// функция определения победителя, получает координаты очередного хода,
// возвращает символ победителя или пробел если победителя ещё нет
char winner(char arr[][3], int x, int y, char player)
{
	//проверяем вертикаль с координатой хода
	bool win = true;
	for (int i = 0; i < 3; ++i)
	{
		if (arr[i][x] != player)
		{
			win = false;
		}
	}
	if (win)
	{
		return player;
	}

	//проверяем горизонталь с координатой хода
	win = true;
	for (int i = 0; i < 3; ++i)
	{
		if (arr[y][i] != player)
		{
			win = false;
		}
	}
	if (win)
	{
		return player;
	}

	//проверяем главную диагональ с координатой хода
	win = true;
	if (x == y) {
		for (int i = 0; i < 3; ++i)
		{
			if (arr[i][i] != player)
			{
				win = false;
			}
		}
		if (win)
		{
			return player;
		}
	}

	//проверяем другую диагональ с координатой хода
	win = true;
	if ((x == y) || (x == 0 && y == 2) || (x == 2 && y == 0)) {
		for (int i = 0; i < 3; ++i)
		{
			if (arr[i][2 - i] != player)
			{
				win = false;
			}
		}
		if (win)
		{
			return player;
		}
	}


	return ' ';
}

